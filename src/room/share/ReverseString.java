package room.share;

public class ReverseString {
	int id;
	String name;
	String team;
	static String company ="CGI";
	
	public static void main(String[] args) {
		
		ReverseString r = new ReverseString();
		System.out.println(r.id = 1001);
		System.out.println(r.name = "hari");
		System.out.println(r.team = "QA");
		System.out.println(company+"\n");
		
		System.out.println(r.id = 1002);
		System.out.println(r.name ="seshu");
		System.out.println(r.team = "QA");
		System.out.println(company+"\n");
		
		System.out.println(r.id =1003);
		System.out.println(r.name = "Anil");
		System.out.println(r.team = "dev");
		System.out.println(company);
		
	}

}
